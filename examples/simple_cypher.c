void hexdump(uint8_t* data, unsigned int len)
{
	char buff[17] = { '\0' };
	for(unsigned int i = 0; i < len; i++)
	{
		printf("%02x ", data[i]);
		buff[i % 16] = isprint(data[i]) ? data[i] : '.';

		if((i + 1) % 16 == 0)
			printf("   %s\n", buff);
	}

	printf("\x1b[52G%.*s\n", len % 16, buff);
}

